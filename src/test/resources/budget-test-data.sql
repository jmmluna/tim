CREATE TABLE IF NOT EXISTS `TIM`.`TIM_BUDGETS` (
  `UUID` UUID NOT NULL,
  `BUDGET_NUMBER` BIGINT NULL,
  `DESCRIPTION` VARCHAR(245) NULL,
  `DATE` DATE NULL,
  `YEAR` INT NULL,
  `EXPIRATION_DATE` DATE NULL,
  PRIMARY KEY (`UUID`),
  UNIQUE INDEX `BUDGET_NUMBER_UNIQUE` (`BUDGET_NUMBER` ASC));


